/** Degradado de vista previa: solo fondo y acento del tema (variables UI). */
export function themePreviewGradient(theme: AppTheme): string {
  const bg = theme.vars['--bg'] ?? theme.xterm.background
  const accent = theme.vars['--accent'] ?? theme.xterm.cursor
  return `linear-gradient(145deg, ${bg} 0%, ${accent} 100%)`
}

export function getTheme(id: string): AppTheme {
  return THEMES.find(t => t.id === id) ?? midnight
}

function isLightTheme(t: AppTheme): boolean {
  return t.appearance === 'light'
}

/**
 * Temas del picker: todos los registrados, oscuros primero y claros después
 * (separador visual entre grupos en el modal).
 */
export function getThemesForPicker(): AppTheme[] {
  const dark = THEMES.filter(t => !isLightTheme(t))
  const light = THEMES.filter(t => isLightTheme(t))
  return [...dark, ...light]
}

export function applyTheme(theme: AppTheme): void {
  const root = document.documentElement
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value)
  }
}
